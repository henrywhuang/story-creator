export const classicStoryItems = classicStoryGroups.flatMap((group, groupIndex) => {
  const titles = [
    ...group.csvTitles.map((title) => ({ title, source: 'csv' })),
    ...group.supplementalTitles.map((title) => ({ title, source: 'supplemental' })),
  ];

  return titles.map((item, index) => ({
    id: `classic-${String(groupIndex + 1).padStart(2, '0')}-${String(index + 1).padStart(2, '0')}`,
    category: group.category,
    title: item.title,
    source: item.source,
    sourceLabel: item.source === 'csv' ? '精选题名' : '补足选题',
    storyBrief:
      item.source === 'csv'
        ? '已按低龄复听率和经典度纳入故事池。'
        : '为补齐 50 集栏目规模加入的经典题名，待教研复核。',
  }));
});

export const classicStoryCategories = classicStoryGroups.map((group) => group.category);
